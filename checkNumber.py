def checkNumber():
    num = int(input("Enter a number: "))
    mod = num % 2
    if mod > 0:
        print("This is an odd number.")
    else:
        print("This is an even number.")


# Press the green button in the gutter to run the script.
if __name__ == '__main__':
    checkNumber()



